package com.android.etuan.whereru.bean;

/**热门活动信息类*/

public class HotCampusActivityInformation {
	
	String id = "";
	String url = "";
	String content = "";
	String type = "";

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

}
